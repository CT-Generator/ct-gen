// /api/build/[id]/yolo — fill in whatever's missing, then stitch the narrative.
//
// Two callers:
//   1. The picker's "skip the walkthrough" CTA — POSTs to a fresh row with
//      empty per_move, so this route generates all four section outputs.
//   2. The wizard's "Skip to result" button — POSTs to a row whose per_move
//      already contains some-but-not-all moves; this route generates only
//      the MISSING moves, preserves the user's earlier choices, then stitches
//      the narrative from the merged four paragraphs.
//
// Idempotent: if the row is already complete (all four moves + narrative),
// returns { ok: true, cached: true } without touching the model. If sections
// are complete but narrative is missing (rare: stepwise build whose narrative
// was moderation-flagged), generates ONLY the narrative.
//
// All-or-nothing for the per_move write: if any new section fails, nothing
// is persisted to per_move (existing entries remain untouched).

import { NextResponse } from "next/server";
import { eq } from "drizzle-orm";
import { db, schema } from "@/lib/db";
import { generateNarrative, generateSection, moderate } from "@/lib/openai";
import { type MoveKey, type WizardContent } from "@/lib/recipe";
import { isLocale, type Locale } from "@/lib/i18n";

export const runtime = "nodejs";
export const dynamic = "force-dynamic";
export const maxDuration = 90;

const MOVE_KEYS: MoveKey[] = ["anomaly", "connection", "dismiss", "discredit"];

type Params = { id: string };
type ExistingMove = { idea: string; paragraph: string; debunk: string };

function pickRandom<T>(arr: readonly T[]): T {
  return arr[Math.floor(Math.random() * arr.length)]!;
}

export async function POST(_req: Request, { params }: { params: Promise<Params> }) {
  const { id } = await params;
  const row = (
    await db()
      .select()
      .from(schema.generations)
      .where(eq(schema.generations.shortId, id))
      .limit(1)
  )[0];
  if (!row) return NextResponse.json({ error: "Not found" }, { status: 404 });

  const content = row.recipeContent as WizardContent;
  const existingPerMove = (content.per_move ?? {}) as Partial<Record<MoveKey, ExistingMove>>;
  const missingKeys = MOVE_KEYS.filter((k) => !existingPerMove[k]);
  const haveNarrative = Boolean(content.narrative?.paragraphs?.length);

  // Idempotent short-circuit: all moves complete AND narrative present.
  if (missingKeys.length === 0 && haveNarrative) {
    return NextResponse.json({ ok: true, cached: true });
  }

  // Validate ideas are present (would have been generated by /api/start).
  // Only checked when we actually need to generate sections.
  if (missingKeys.length > 0) {
    if (
      !content.ideas ||
      !MOVE_KEYS.every((k) => Array.isArray(content.ideas?.[k]) && content.ideas[k].length > 0)
    ) {
      return NextResponse.json(
        { error: "Build is not ready — ideas missing. Run /api/start first." },
        { status: 409 },
      );
    }
  }

  const rowLocale: Locale = isLocale(row.locale) ? row.locale : "en";

  // Random picks ONLY for missing moves; existing moves keep the user's idea verbatim.
  const newPicks: Partial<Record<MoveKey, string>> = {};
  for (const k of missingKeys) {
    newPicks[k] = pickRandom(content.ideas![k]);
  }

  // Fan out section generation only for missing moves.
  let newSections: Partial<Record<MoveKey, { paragraph: string; debunk: string }>> = {};
  if (missingKeys.length > 0) {
    try {
      const generated = await Promise.all(
        missingKeys.map((moveKey) =>
          generateSection({
            locale: rowLocale,
            eventName: row.eventValue,
            eventSummary: content.event_intro?.paragraphs?.join("\n\n") ?? "",
            culpritName: row.culpritValue,
            motiveName: row.motiveValue,
            moveKey,
            chosenIdea: newPicks[moveKey]!,
            // Yolo (full or partial) runs in parallel — no deterministic earlier-move ordering.
            prior: {},
          }),
        ),
      );
      for (let i = 0; i < missingKeys.length; i++) {
        newSections[missingKeys[i]!] = generated[i]!;
      }
    } catch (err) {
      console.error("[yolo] section generation failed:", err);
      return NextResponse.json(
        { error: "The theory engine glitched mid-build — try again." },
        { status: 502 },
      );
    }

    // Moderate only the new paragraphs (existing ones already passed moderation).
    const mods = await Promise.all(
      missingKeys.map((k) =>
        moderate(newSections[k]!.paragraph).catch(() => ({ flagged: false })),
      ),
    );
    const flaggedIdx = mods.findIndex((m) => m.flagged);
    if (flaggedIdx >= 0) {
      console.warn("[yolo] section flagged", { id, moveKey: missingKeys[flaggedIdx] });
      return NextResponse.json(
        { error: "The engine refused this combo. Try again or pick different conspirators." },
        { status: 422 },
      );
    }
  }

  // Build the merged per_move: existing entries first (preserved verbatim), then add new entries.
  const mergedPerMove: NonNullable<WizardContent["per_move"]> = { ...existingPerMove };
  for (const k of missingKeys) {
    mergedPerMove[k] = {
      idea: newPicks[k]!,
      paragraph: newSections[k]!.paragraph,
      debunk: newSections[k]!.debunk,
    };
  }

  // Narrative finale: regenerate when missing OR when we just filled in moves.
  // (When all moves were already complete and narrative exists, we returned early above.)
  let narrative: WizardContent["narrative"] | undefined = content.narrative;
  if (!haveNarrative || missingKeys.length > 0) {
    try {
      const out = await generateNarrative({
        locale: rowLocale,
        eventName: row.eventValue,
        culpritName: row.culpritValue,
        motiveName: row.motiveValue,
        paragraphs: {
          anomaly: mergedPerMove.anomaly!.paragraph,
          connection: mergedPerMove.connection!.paragraph,
          dismiss: mergedPerMove.dismiss!.paragraph,
          discredit: mergedPerMove.discredit!.paragraph,
        },
      });
      const joined = out.paragraphs.join("\n\n");
      const mod = await moderate(joined).catch(() => ({ flagged: false }));
      if (mod.flagged) {
        console.warn("[yolo] narrative flagged, skipping persistence", { id });
        narrative = undefined;
      } else {
        narrative = { paragraphs: out.paragraphs, generated_at: new Date().toISOString() };
      }
    } catch (err) {
      console.error("[yolo] narrative generation failed:", err);
      // Keep existing narrative if any; otherwise leave undefined.
    }
  }

  const newContent: WizardContent = {
    ...content,
    per_move: mergedPerMove,
    ...(narrative ? { narrative } : {}),
  };

  await db()
    .update(schema.generations)
    .set({ recipeContent: newContent })
    .where(eq(schema.generations.shortId, id));

  return NextResponse.json({ ok: true });
}
